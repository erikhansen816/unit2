grade = float(input('Input your grade: '))
if grade >= 90:
    print('You earned an A')
elif grade >= 80:
    print('You earned a B')
elif grade >= 70:
    print('You earned a C')
elif grade >= 60:
    print('You earned a D')
elif grade < 60:
    print('You earned a F')
    

